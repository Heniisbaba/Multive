<?php


namespace MultiveCLI;


use Symfony\Component\Console\Command\Command;
use Symfony\Component\Console\Input\InputInterface;
use Symfony\Component\Console\Output\OutputInterface;

class ProductionCommand extends Command {

    protected function configure (): void {
        parent::configure(); // TODO: Change the autogenerated stub
        $this->setName('production')->setDescription("This command will delete the cache and errorLog files.");
    }

    protected function execute (InputInterface $input, OutputInterface $output): int {
        if (file_exists('cache/errors.log')) {
            unlink('cache/errors.log');
        }

        if (file_exists('cache/cache.php')) {
            unlink('cache/cache.php');
        }

        $output->writeln('<info>Completed!</info>');
        return 0;
    }

}
